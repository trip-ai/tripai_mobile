  factory WeatherLocationModel.fromJson(Map<String, dynamic> json) =>
      WeatherLocationModel(
        name: json["name"],
        region: json["region"],
        country: json["country"],
        lat: json["lat"]?.toDouble(),
        lon: json["lon"]?.toDouble(),
        tzId: json["tz_id"],
